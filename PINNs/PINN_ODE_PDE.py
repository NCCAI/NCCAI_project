import torch
import torch.nn as nn
import torch.autograd as autograd
import matplotlib.pyplot as plt
import numpy as np

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

# --- Neural network ---
class PINN(nn.Module):
    def __init__(self, layers):
        super(PINN, self).__init__()
        self.linears = nn.ModuleList(
            [nn.Linear(layers[i], layers[i+1]) for i in range(len(layers)-1)]
        )
        self.activation = nn.Tanh()
        for lin in self.linears:
            nn.init.xavier_normal_(lin.weight)
            nn.init.zeros_(lin.bias)

    def forward(self, x, t):
        X = torch.cat((x, t), dim=1)
        a = X
        for i in range(len(self.linears)-1):
            a = self.activation(self.linears[i](a))
        return self.linears[-1](a)

# --- PDE residual: u_t - Î± u_xx = 0 ---
def physics_residual(model, x, t, alpha=1.0):
    x.requires_grad = True
    t.requires_grad = True
    u = model(x, t)
    u_t = autograd.grad(u, t, torch.ones_like(u), create_graph=True)[0]
    u_x = autograd.grad(u, x, torch.ones_like(u), create_graph=True)[0]
    u_xx = autograd.grad(u_x, x, torch.ones_like(u_x), create_graph=True)[0]
    return u_t - alpha * u_xx

# --- Initial and boundary conditions ---
def initial_condition(x):
    return torch.sin(np.pi * x)

def train_model(lambda_phys=1.0, epochs=3000):
    model = PINN([2, 50, 50, 50, 1]).to(device)
    optimizer = torch.optim.Adam(model.parameters(), lr=1e-3)

    # Collocation points (physics)
    N_f = 2000
    x_f = torch.rand((N_f,1))
    t_f = torch.rand((N_f,1))

    # Boundary points
    N_b = 100
    x0 = torch.zeros((N_b,1))
    x1 = torch.ones((N_b,1))
    t_b = torch.rand((N_b,1))

    # Initial condition points
    N_i = 100
    x_i = torch.linspace(0,1,N_i).view(-1,1)
    t_i = torch.zeros((N_i,1))
    u_i = initial_condition(x_i)

    for epoch in range(epochs):
        optimizer.zero_grad()

        # Physics loss
        f = physics_residual(model, x_f.to(device), t_f.to(device))
        loss_f = torch.mean(f**2)

        # Boundary losses
        u_b0 = model(x0.to(device), t_b.to(device))
        u_b1 = model(x1.to(device), t_b.to(device))
        loss_b = torch.mean(u_b0**2) + torch.mean(u_b1**2)

        # Initial loss
        u_pred_i = model(x_i.to(device), t_i.to(device))
        loss_i = torch.mean((u_pred_i - u_i.to(device))**2)

        # Total loss
        loss = lambda_phys*loss_f + loss_b + loss_i
        loss.backward()
        optimizer.step()

        if epoch % 500 == 0:
            print(f"Epoch {epoch}, Î»_phys={lambda_phys}, Loss={loss.item():.6f}")

    return model

# --- Prediction function ---
def predict(model, x, t):
    with torch.no_grad():
        u_pred = model(x.to(device), t.to(device)).cpu().numpy()
    return u_pred

# --- Setup grid for evaluation ---
