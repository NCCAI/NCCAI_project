import torch
import torch.nn as nn
import torch.autograd as autograd

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

# Define a simple feed-forward network
class PINN(nn.Module):
    def __init__(self, layers):
        super(PINN, self).__init__()
        self.linears = nn.ModuleList([nn.Linear(layers[i], layers[i+1])
                                      for i in range(len(layers)-1)])
        self.activation = nn.Tanh()

        # Xavier initialization
        for lin in self.linears:
            nn.init.xavier_normal_(lin.weight.data, gain=1.0)
            nn.init.zeros_(lin.bias.data)

    def forward(self, x):
        a = x
        for i in range(len(self.linears)-1):
            a = self.activation(self.linears[i](a))
        a = self.linears[-1](a)
        return a

# Example: Solve dy/dx = cos(x), y(0) = 0  (so true solution y = sin(x))
def residual(model, x):
    x = x.clone().requires_grad_(True).to(device)
    y = model(x)
    dy_dx = autograd.grad(y, x, torch.ones_like(y), create_graph=True)[0]
    return dy_dx - torch.cos(x)

# Boundary condition loss
def bc_loss(model, x0, y0):
    x0 = x0.to(device)
    y0 = y0.to(device)
    pred = model(x0)
    return torch.mean((pred - y0)**2)

# Training setup
model = PINN(layers=[1, 50, 50, 50, 1]).to(device)
optimizer = torch.optim.Adam(model.parameters(), lr=1e-3)
epochs = 15000

# Sample points in domain for residual
x_collocation = torch.linspace(0, 2*torch.pi, 100).view(-1,1)

# Boundary point
x0 = torch.tensor([[0.0]])
y0 = torch.tensor([[0.0]])

for epoch in range(epochs):
    optimizer.zero_grad()
    res = residual(model, x_collocation)
    loss_r = torch.mean(res**2)
    loss_bc = bc_loss(model, x0, y0)
    loss =loss_r + loss_bc
    loss.backward()
    optimizer.step()
    if epoch % 500 == 0:
        print(f"Epoch {epoch}, Loss: {loss.item():.6f}")

import matplotlib.pyplot as plt
import numpy as np

# Test
x_test = torch.linspace(0, 2*torch.pi, 100).view(-1,1).to(device)
y_pred = model(x_test).detach().cpu().numpy()
